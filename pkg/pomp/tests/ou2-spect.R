library(pomp)
pompExample(ou2)

set.seed(362083261L)

pdf(file="ou2-spect.pdf")

gm1 <- spect.match(ou2,
                  kernel.width=3,
                  detrend="mean",
                  nsim=50,
                  est=c("alpha.1","alpha.4"),
                  method="Nelder-Mead")
gm1@value
plot(gm1)

